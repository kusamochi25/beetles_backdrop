export function normalizeLocale(value: string | null | undefined): Locale {
    const candidate = value?.toLowerCase();

    if (candidate === 'ja' || candidate === 'en' || candidate === 'zh' || candidate === 'ko' || candidate === 'es' || candidate === 'th' || candidate === 'fr') {
        return candidate;
    }

    return 'ja';
}

export function getLocalizedText(value: LocalizedText | undefined, locale: Locale) {
    if (!value) {
        return '';
    }

    return value[locale] || value.ja || value.en || Object.values(value).find(Boolean) || '';
}

export function getLocalizedArray(value: LocalizedArray | undefined, locale: Locale) {
    if (!value) {
        return [];
    }

    return value[locale]?.length ? value[locale] : value.ja?.length ? value.ja : value.en ?? [];
}

